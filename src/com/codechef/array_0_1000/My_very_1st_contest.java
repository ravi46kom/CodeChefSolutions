package com.codechef.array_0_1000;

import java.util.Scanner;

public class My_very_1st_contest {

	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		int n = s.nextInt();
		int a = s.nextInt();
		int b = s.nextInt();
		System.out.println(n - a + " " + (n - a - b));
	}

}
