package com.codechef.math_0_1000;

import java.util.Scanner;

public class Tyre_problem {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		while (t-- > 0) {
			int n = sc.nextInt() * 2;
			int m = sc.nextInt() * 4;
			System.out.println(n + m);
		}
	}

}
