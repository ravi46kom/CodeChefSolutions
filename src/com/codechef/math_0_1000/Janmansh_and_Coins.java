package com.codechef.math_0_1000;

import java.util.Scanner;

public class Janmansh_and_Coins {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		while (t-- > 0) {
			int x = sc.nextInt() * 10;
			int y = sc.nextInt() * 5;
			System.out.println(x + y);
		}
	}

}
